package obj;

public class PgLingueObj {
	boolean datiVariati = false;
	int puntiAbilita = 0;
	String linguaParlata = "";
	String alfabeto = "";
	int prg = 0;
	public boolean isDatiVariati() {
		return datiVariati;
	}
	public void setDatiVariati(boolean datiVariati) {
		this.datiVariati = datiVariati;
	}
	public int getPuntiAbilita() {
		return puntiAbilita;
	}
	public void setPuntiAbilita(int puntiAbilita) {
		this.puntiAbilita = puntiAbilita;
	}
	public String getLinguaParlata() {
		return linguaParlata;
	}
	public void setLinguaParlata(String linguaParlata) {
		this.linguaParlata = linguaParlata;
	}
	public String getAlfabeto() {
		return alfabeto;
	}
	public void setAlfabeto(String alfabeto) {
		this.alfabeto = alfabeto;
	}
	public int getPrg() {
		return prg;
	}
	public void setPrg(int prg) {
		this.prg = prg;
	}

	
	
}
